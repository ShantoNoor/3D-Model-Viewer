package com.modelviewer.Renderer;

import com.modelviewer.Renderer.Shader.ShaderProgram;

import static org.lwjgl.opengl.GL11.*;

public class Cube {
    float skyboxVertices[] = {
            // positions
            -1.0f,  1.0f, -1.0f,
            -1.0f, -1.0f, -1.0f,
            1.0f, -1.0f, -1.0f,
            1.0f, -1.0f, -1.0f,
            1.0f,  1.0f, -1.0f,
            -1.0f,  1.0f, -1.0f,

            -1.0f, -1.0f,  1.0f,
            -1.0f, -1.0f, -1.0f,
            -1.0f,  1.0f, -1.0f,
            -1.0f,  1.0f, -1.0f,
            -1.0f,  1.0f,  1.0f,
            -1.0f, -1.0f,  1.0f,

            1.0f, -1.0f, -1.0f,
            1.0f, -1.0f,  1.0f,
            1.0f,  1.0f,  1.0f,
            1.0f,  1.0f,  1.0f,
            1.0f,  1.0f, -1.0f,
            1.0f, -1.0f, -1.0f,

            -1.0f, -1.0f,  1.0f,
            -1.0f,  1.0f,  1.0f,
            1.0f,  1.0f,  1.0f,
            1.0f,  1.0f,  1.0f,
            1.0f, -1.0f,  1.0f,
            -1.0f, -1.0f,  1.0f,

            -1.0f,  1.0f, -1.0f,
            1.0f,  1.0f, -1.0f,
            1.0f,  1.0f,  1.0f,
            1.0f,  1.0f,  1.0f,
            -1.0f,  1.0f,  1.0f,
            -1.0f,  1.0f, -1.0f,

            -1.0f, -1.0f, -1.0f,
            -1.0f, -1.0f,  1.0f,
            1.0f, -1.0f, -1.0f,
            1.0f, -1.0f, -1.0f,
            -1.0f, -1.0f,  1.0f,
            1.0f, -1.0f,  1.0f
    };

    private VAO vao;
    private VBO vbo;

    public Cube() {
        vao = new VAO();
        vbo = new VBO();
        vbo.uploadVertexAttributeData(vao, skyboxVertices, 0, 3, BufferDataType.STATIC);
    }

    public void render(ShaderProgram shaderProgram) {
        shaderProgram.bind();
        vao.bind();
        vbo.bind();
        glDrawArrays(GL_TRIANGLES, 0, 36);
        vbo.unbind();
        vao.unbind();
    }

    public void clear() {
        vao.clear();
        vbo.clear();
        vao = null;
        vbo = null;
        skyboxVertices = null;
    }
}
